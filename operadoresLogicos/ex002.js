// Verifique se um número digitado está entre 10 e 20 (inclusive)
// Pergunte um número e mostre se está dentro do intervalo especificado.

// Exemplo:
// Input: 15
// Output: true

// Input: 8
// Output: false

var num = 15;

if (num >= 10 && num <= 20) {
  console.log("Está dentro");
} else {
  console.log("Está fora");
}
